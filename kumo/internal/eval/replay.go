// Package eval implements traffic replay for policy evaluation and refinement.
package eval

import (
	"bufio"
	"encoding/json"
	"fmt"
	"net/http"
	"os"
	"path/filepath"
	"strings"
	"time"

	"github.com/garrytan/kumo/internal/policy"
	"github.com/garrytan/kumo/pkg/types"
)

// ReplayResult is the outcome of replaying one request against a policy.
type ReplayResult struct {
	Entry          types.TrafficEntry
	OrigDecision   string
	ReplayDecision types.Decision
	ReplayRule     string
	ReplayReason   string
	Match          bool // true if original and replay decisions agree
}

// ReplaySummary is the aggregate result of replaying a log file.
type ReplaySummary struct {
	TotalRequests int
	Passed        int
	Failed        int
	WouldBlock    int // currently allowed, policy would block
	WouldAllow    int // currently blocked, policy would allow
	Flagged       int
	Results       []ReplayResult
	Duration      time.Duration
}

// Replay reads a traffic log and evaluates each request against the given policy.
func Replay(logPath string, policyPath string) (*ReplaySummary, error) {
	// Load policy
	pol, err := policy.LoadPolicy(policyPath)
	if err != nil {
		return nil, fmt.Errorf("Error: cannot load policy\nCause: %v\nFix: check that %s is valid YAML", err, policyPath)
	}

	engine, err := policy.NewEngine(pol)
	if err != nil {
		return nil, fmt.Errorf("Error: cannot compile policy\nCause: %v", err)
	}

	// Read traffic entries
	entries, err := readLogFile(logPath)
	if err != nil {
		return nil, err
	}

	if len(entries) == 0 {
		return nil, fmt.Errorf("Error: no traffic entries in %s\nCause: file is empty or contains no valid JSONL\nFix: check that the file was generated by 'kumo serve --mode observe'", logPath)
	}

	start := time.Now()
	summary := &ReplaySummary{
		TotalRequests: len(entries),
	}

	for _, entry := range entries {
		// Reconstruct an http.Request for the engine
		req, _ := http.NewRequest(entry.Method, entry.URL, nil)
		if req.URL.Host == "" {
			req.URL.Host = entry.Host
		}
		req.URL.Path = entry.Path
		req.Host = entry.Host

		// Evaluate against policy
		decision := engine.Evaluate(req, entry.GatewayToken)

		result := ReplayResult{
			Entry:          entry,
			OrigDecision:   entry.Decision,
			ReplayDecision: decision.Decision,
			ReplayRule:     decision.Rule,
			ReplayReason:   decision.Reason,
			Match:          entry.Decision == decision.Decision.String(),
		}

		if result.Match {
			summary.Passed++
		} else {
			summary.Failed++
			if decision.Decision == types.DecisionBlock && entry.Decision == "allow" {
				summary.WouldBlock++
			}
			if decision.Decision == types.DecisionAllow && entry.Decision == "block" {
				summary.WouldAllow++
			}
		}
		if decision.Decision == types.DecisionFlag {
			summary.Flagged++
		}

		summary.Results = append(summary.Results, result)
	}

	summary.Duration = time.Since(start)
	return summary, nil
}

// PrintSummary outputs the replay results to stdout.
func PrintSummary(s *ReplaySummary) {
	fmt.Printf("Replaying %d requests...\n", s.TotalRequests)
	passRate := float64(s.Passed) / float64(s.TotalRequests) * 100
	fmt.Printf("  PASS: %d (%.1f%%)\n", s.Passed, passRate)
	fmt.Printf("  FAIL: %d (%.1f%%)\n", s.Failed, 100-passRate)
	if s.WouldBlock > 0 {
		fmt.Printf("    - %d requests would have been blocked (currently allowed)\n", s.WouldBlock)
	}
	if s.WouldAllow > 0 {
		fmt.Printf("    - %d requests would have been allowed (currently blocked)\n", s.WouldAllow)
	}
	if s.Flagged > 0 {
		fmt.Printf("    - %d requests flagged for review\n", s.Flagged)
	}
	fmt.Printf("  Duration: %v\n", s.Duration.Round(time.Millisecond))
}

// WriteReport writes an eval report to a markdown file.
func WriteReport(s *ReplaySummary, outputPath string) error {
	var b strings.Builder

	b.WriteString(fmt.Sprintf("# Eval Report\n\nGenerated: %s\n\n", time.Now().Format(time.RFC3339)))
	b.WriteString(fmt.Sprintf("## Summary\n\n"))
	b.WriteString(fmt.Sprintf("- Total requests: %d\n", s.TotalRequests))
	b.WriteString(fmt.Sprintf("- Passed: %d (%.1f%%)\n", s.Passed, float64(s.Passed)/float64(s.TotalRequests)*100))
	b.WriteString(fmt.Sprintf("- Failed: %d\n", s.Failed))
	b.WriteString(fmt.Sprintf("- Would block: %d\n", s.WouldBlock))
	b.WriteString(fmt.Sprintf("- Would allow: %d\n", s.WouldAllow))
	b.WriteString(fmt.Sprintf("- Flagged: %d\n\n", s.Flagged))

	if s.Failed > 0 {
		b.WriteString("## Mismatches\n\n")
		b.WriteString("| Method | Host | Path | Original | Replay | Rule |\n")
		b.WriteString("|--------|------|------|----------|--------|------|\n")
		for _, r := range s.Results {
			if !r.Match {
				b.WriteString(fmt.Sprintf("| %s | %s | %s | %s | %s | %s |\n",
					r.Entry.Method, r.Entry.Host, truncate(r.Entry.Path, 40),
					r.OrigDecision, r.ReplayDecision, r.ReplayRule))
			}
		}
	}

	dir := filepath.Dir(outputPath)
	if err := os.MkdirAll(dir, 0755); err != nil {
		return err
	}
	return os.WriteFile(outputPath, []byte(b.String()), 0644)
}

func readLogFile(path string) ([]types.TrafficEntry, error) {
	file, err := os.Open(path)
	if err != nil {
		return nil, fmt.Errorf("open %s: %w", path, err)
	}
	defer file.Close()

	var entries []types.TrafficEntry
	scanner := bufio.NewScanner(file)
	scanner.Buffer(make([]byte, 1024*1024), 1024*1024)
	for scanner.Scan() {
		var entry types.TrafficEntry
		if err := json.Unmarshal(scanner.Bytes(), &entry); err != nil {
			continue
		}
		entries = append(entries, entry)
	}
	return entries, nil
}

func truncate(s string, n int) string {
	if len(s) <= n {
		return s
	}
	return s[:n-3] + "..."
}
